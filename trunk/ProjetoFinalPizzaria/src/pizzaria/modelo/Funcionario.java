package pizzaria.modelo;

public class Funcionario {

    private int fun_mat;
    private String fun_nome;
    private String fun_tel;

    public int getFun_mat() {
        return fun_mat;
    }

    public void setFun_mat(int fun_mat) {
        this.fun_mat = fun_mat;
    }

    public String getFun_nome() {
        return fun_nome;
    }

    public void setFun_nome(String fun_nome) {
        this.fun_nome = fun_nome;
    }

    public String getFun_tel() {
        return fun_tel;
    }

    public void setFun_tel(String fun_tel) {
        this.fun_tel = fun_tel;
    }
}
